package com.hotelManagement.models;

public class Hotel {
	
	String hotelId;
	String hotelName;
	String country;
	String city;
	String hotelDescription;
	int numberOfAcRooms;
	int ratePerNightForAdultInAC;
	int ratePerNightForChildInAC;
	int ratePerNightForAdultInNonAC;
	int ratePerNightForChildInNonAC;
	
	public String getHotelId() {
		return hotelId;
	}
	public void setHotelId(String hotelId) {
		this.hotelId = hotelId;
	}
	public String getHotelName() {
		return hotelName;
	}
	public void setHotelName(String hotelName) {
		this.hotelName = hotelName;
	}
	public String getCountry() {
		return country;
	}
	public void setCountry(String country) {
		this.country = country;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getHotelDescription() {
		return hotelDescription;
	}
	public void setHotelDescription(String hotelDescription) {
		this.hotelDescription = hotelDescription;
	}
	public int getNumberOfAcRooms() {
		return numberOfAcRooms;
	}
	public void setNumberOfAcRooms(int numberOfAcRooms) {
		this.numberOfAcRooms = numberOfAcRooms;
	}
	public int getRatePerNightForAdultInAC() {
		return ratePerNightForAdultInAC;
	}
	public void setRatePerNightForAdultInAC(int ratePerNightForAdultInAC) {
		this.ratePerNightForAdultInAC = ratePerNightForAdultInAC;
	}
	public int getRatePerNightForChildInAC() {
		return ratePerNightForChildInAC;
	}
	public void setRatePerNightForChildInAC(int ratePerNightForChildInAC) {
		this.ratePerNightForChildInAC = ratePerNightForChildInAC;
	}
	public int getRatePerNightForAdultInNonAC() {
		return ratePerNightForAdultInNonAC;
	}
	public void setRatePerNightForAdultInNonAC(int ratePerNightForAdultInNonAC) {
		this.ratePerNightForAdultInNonAC = ratePerNightForAdultInNonAC;
	}
	public int getRatePerNightForChildInNonAC() {
		return ratePerNightForChildInNonAC;
	}
	public void setRatePerNightForChildInNonAC(int ratePerNightForChildInNonAC) {
		this.ratePerNightForChildInNonAC = ratePerNightForChildInNonAC;
	}
	
	
	
}
